package com.bz.hos.model.NewOrderForm;

public class NewRecord {

    String accountOrdered;

    String jobName;

    String contract;

    String workingFrom;

    String workingTo;

    String paymentValue;

    String paymentType;

    String qualification;

    String comment;

    public String getAccountOrdered() {
        return accountOrdered;
    }

    public void setAccountOrdered(String accountOrdered) {
        this.accountOrdered = accountOrdered;
    }

    public String getJobName() {
        return jobName;
    }

    public void setJobName(String jobName) {
        this.jobName = jobName;
    }

    public String getWorkingFrom() {
        return workingFrom;
    }

    public void setWorkingFrom(String workingFrom) {
        this.workingFrom = workingFrom;
    }

    public String getWorkingTo() {
        return workingTo;
    }

    public void setWorkingTo(String workingTo) {
        this.workingTo = workingTo;
    }

    public String getPaymentValue() {
        return paymentValue;
    }

    public void setPaymentValue(String paymentValue) {
        this.paymentValue = paymentValue;
    }

    public String getPaymentType() {
        return paymentType;
    }

    public void setPaymentType(String paymentType) {
        this.paymentType = paymentType;
    }

    public String getContract() {
        return contract;
    }

    public void setContract(String contract) {
        this.contract = contract;
    }

    public String getQualification() {
        return qualification;
    }

    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

}
